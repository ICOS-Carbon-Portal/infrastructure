import { type TaskFile } from "../../../lib/ansible.ts";
import { tmpl, V } from "../_ctx.ts";

export default [
  {
    name: "Install rsync",
    apt: { name: ["rsync"] },
  },
  {
    name: "Add config block to /etc/rsyncd.conf",
    copy: {
      dest: "/etc/rsyncd.conf",
      content: tmpl("{{ rsyncd_conf }}"),
    },
    notify: "restart rsync",
  },
  {
    name: "Make sure that /etc/rsyncd.secrets exists",
    lineinfile: {
      path: "/etc/rsyncd.secrets",
      mode: 0o600,
      line: "# this file is partly generated by ansible",
      create: true,
      insertbefore: "BOF",
    },
  },
  {
    name: "Start rsync service",
    systemd: {
      name: "rsync",
      enabled: V.rsyncd_enable,
      state: tmpl("{{ 'started' if rsyncd_enable else 'stopped' }}"),
    },
  },
  {
    name: "Copy justfile",
    template: {
      src: "ops-rsyncd",
      dest: "/usr/local/bin/",
      mode: "+x",
      variable_start_string: "((",
      variable_end_string: "))",
      lstrip_blocks: true,
    },
    register: "_justfile",
  },
  {
    name: "Check that the justfile is executable",
    shell: tmpl("{{ _justfile.dest }}"),
    changed_when: false,
  },
] satisfies TaskFile;
